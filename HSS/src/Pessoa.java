public class Pessoa {
    String nome;
    int idade;
    String tipoSanguineo;
    float peso;
    float altura;
    String sexo;
    boolean apto;
    boolean doador;

    public Pessoa(String nome, int idade, String tipoSanguineo, float peso, float altura, String sexo, boolean apto, boolean doador) {
        this.nome = nome;
        this.idade = idade;
        this.tipoSanguineo = tipoSanguineo;
        this.peso = peso;
        this.altura = altura;
        this.sexo = sexo;
        this.apto = apto;
        this.doador = doador;
    }




}
